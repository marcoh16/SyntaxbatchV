package com.class7;

public class WhileLoop {

	public static void main(String[] args) {
		
		int time = 8;
		while (time<12) {
			System.out.println("morning");
			time++;
			
			
		}
		
		
		System.out.println("---------------");
		
		int morning = 1;
		
		while(morning < 6) {
			System.out.println("morning");
			morning++;
		}
		
		System.out.println("---------------");
		
		int i=12;
		while (i<17) {
			System.out.println("good afternoon");
			i++;
		}
		System.out.println("outside of while loop");
		
		
		
		
	}
	
	
}
