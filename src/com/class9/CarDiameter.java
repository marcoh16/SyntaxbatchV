package com.class9;

public class CarDiameter {
	public static void main(String[] args) {
		
		for (int i=0; i<=10; i++ ) {
			for (int b=0; b<=10; b++) {
				for (int c=0; c<=10; c++) {
					for (int d=0; d<=10; d++) {
						System.out.println(i + " " + b + " " + c + " " + d);
					}
				}
				
			}
		}
		
	}

}

